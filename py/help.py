import random

flag = [0x27,0x0c,0x0f,0x0c,0x2c,0x39,0x23,0x5b,
              0x31,0x5b,0x53,0x29,0x16,0x34,0x7e,0x4e,
              0x3d,0x10,0x1d,0x67,0x3a,0x06,0x16,0x1c,
              0x19,0x3a,0x58,0x64,0x1d,0x5d]

sbox = [random.randint(0x0, 0xff) for _ in range(512)]

pos = []

for i in range(30):
  r = random.randint(0, 511)
  while r in pos:
    r = random.randint(0, 511)
  pos.append(r)

for i in range(len(flag)):
  sbox[pos[i]] = flag[i]

print(',0x'.join(format(x, '02x') for x in sbox))
print(pos)